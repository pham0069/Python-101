# arithmetic
a = 10
b = 3
print(a+b)

print(a/b)

# integer division
print(a//b)

# exponentiation
print(a**b)

# comparison: ==, <, <=, >, >=
print(a<b)

# assignment: =, +=, *=, -=, /=
a += 2
print(a)

# logical: and, or, not
if a < 0 and b < 0:
    print("Both numbers are negative")
else:
    print("Either number is not-negative")

c = False
print(not c)
